            end
        else
            tooltip:AddLine(("掉落：" .. nodes[mapFile][coord][3]), nil, nil, nil, true)
        end
    end

    if (nodes[mapFile][coord][4] ~= "") and (LegionRaresTreasures.db.profile.show_notes == true) then
     tooltip:AddLine(("注释：" .. nodes[mapFile][coord][4]), nil, nil, nil, true)
    end

    tooltip:Show()
end

